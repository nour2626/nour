$categoriesList = $categoriesController->categoriesList();

// Get locations list from the database
$conn = config::getConnexion();
$locationsQuery = $conn->query("SELECT id, nom FROM locations");
$locationsList = $locationsQuery->fetchAll(PDO::FETCH_ASSOC);

// Initialize variables to avoid errors
$nom_site = $description_site = $category = $images = $location = "";
$errors = [];

// Check if the form is submitted
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Validate fields
    if (empty($_POST['nom_site'])) {
        $errors[] = "Le nom du site est obligatoire.";
    } else {
        $nom_site = htmlspecialchars($_POST['nom_site']);
    }

    if (empty($_POST['description_site'])) {
        $errors[] = "La description du site est obligatoire.";
    } else {
        $description_site = htmlspecialchars($_POST['description_site']);
    }

    if (empty($_POST['category'])) {
        $errors[] = "La catégorie est obligatoire.";
    } else {
        $category = $_POST['category'];
    }

    if (empty($_POST['location'])) {
        $errors[] = "La localisation est obligatoire.";
    } else {
        $location = $_POST['location'];
    }

    if (isset($_FILES['images']) && $_FILES['images']['error'] === 0) {
        // Handle uploaded image
        $images = $_FILES['images']['name'];
        $target_dir = "uploads/";
        $target_file = $target_dir . basename($images);

        if (move_uploaded_file($_FILES["images"]["tmp_name"], $target_file)) {
            // Image uploaded successfully
        } else {
            $errors[] = "L'image n'a pas pu être téléchargée.";
        }
    } else {
        $images = null; // Leave empty if no image is uploaded
    }

    // If no errors found, add the site
    if (empty($errors)) {
        // Create an instance of the Sites class
        $site = new Sites(0, $nom_site, $description_site, $category, $images, $location);

        // Add the site to the database using the controller
        $sitesController = new SitesController();
        $sitesController->addSite($site);

        // Redirect to sitesList.php after successful addition
        header("Location: sitesList.php");
        exit(); // Make sure no further code is executed after the redirection
    }
}
